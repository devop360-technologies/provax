import { cn } from "@/lib/utils"

const SidebarContext = React.createContext<{
  state: "expanded" | "collapsed"
  open: boolean
  setOpen: (open: boolean) => void
  openMobile: boolean
  setOpenMobile: (open: boolean) => void
  isMobile: boolean
  toggleSidebar: () => void
}>({} as any)

export function SidebarProvider({
  defaultOpen = true,
  open: openProp,
  onOpenChange: setOpenProp,
  className,
  style,
  children,
  ...props
}: React.ComponentProps<"div"> & {
  defaultOpen?: boolean
  open?: boolean
  onOpenChange?: (open: boolean) => void
}) {
  const isMobile = false // Simplified for now
  const [openMobile, setOpenMobile] = React.useState(false)
  const [_open, _setOpen] = React.useState(defaultOpen)
  const open = openProp ?? _open
  const setOpen = React.useCallback(
    (value: boolean | ((value: boolean) => boolean)) => {
      const openState = typeof value === "function" ? value(open) : value
      if (setOpenProp) {
        setOpenProp(openState)
      } else {
        _setOpen(openState)
      }
    },
    [setOpenProp, open]
  )

  const toggleSidebar = React.useCallback(() => {
    return isMobile
      ? setOpenMobile((open) => !open)
      : setOpen((open) => !open)
  }, [isMobile, setOpen, setOpenMobile])

  const state = open ? "expanded" : "collapsed"

  const contextValue = React.useMemo(
    () => ({
      state,
      open,
      setOpen,
      isMobile,
      openMobile,
      setOpenMobile,
      toggleSidebar,
    }),
    [state, open, setOpen, isMobile, openMobile, setOpenMobile, toggleSidebar]
  )

  return (
    <SidebarContext.Provider value={contextValue}>
      <div
        style={{
          "--sidebar-width": "16rem",
          "--sidebar-width-mobile": "18rem",
          ...style,
        } as React.CSSProperties}
        className={cn(
          "group/sidebar-wrapper flex min-h-svh w-full has-[[data-variant=inset]]:bg-sidebar",
          className
        )}
        {...props}
      >
        {children}
      </div>
    </SidebarContext.Provider>
  )
}

export const useSidebar = () => {
  const context = React.useContext(SidebarContext)
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider.")
  }
  return context
}

const sidebarVariants = cva(
  "flex h-full w-full flex-col bg-sidebar text-sidebar-foreground",
  {
    variants: {
      variant: {
        default: "border-r",
        floating:
          "border border-sidebar-border bg-sidebar shadow-lg rounded-lg",
        inset: "bg-background",
      },
      side: {
        left: "",
        right: "border-l",
      },
    },
    defaultVariants: {
      variant: "default",
      side: "left",
    },
  }
)

export interface SidebarProps
  extends React.ComponentProps<"div">,
    VariantProps<typeof sidebarVariants> {
  side?: "left" | "right"
}

const Sidebar = React.forwardRef<HTMLDivElement, SidebarProps>(
  ({ side = "left", variant = "default", className, children, ...props }, ref) => {
    const { isMobile, state, openMobile, setOpenMobile } = useSidebar()

    if (isMobile) {
      return (
        <div className="fixed inset-0 z-50 md:hidden">
          <div
            className={cn(
              "fixed inset-y-0 z-50 w-[var(--sidebar-width-mobile)] bg-sidebar transition-transform duration-200 ease-in-out",
              side === "left" ? "left-0" : "right-0",
              openMobile ? "translate-x-0" : side === "left" ? "-translate-x-full" : "translate-x-full"
            )}
          >
            <div
              ref={ref}
              className={cn(sidebarVariants({ side, variant }), className)}
              {...props}
            >
              {children}
            </div>
          </div>
          <div
            className="fixed inset-0 bg-black/50"
            onClick={() => setOpenMobile(false)}
          />
        </div>
      )
    }

    return (
      <div
        ref={ref}
        className={cn(
          "relative h-svh w-[var(--sidebar-width)] transition-[width] duration-200 ease-in-out",
          state === "collapsed" && "w-[calc(var(--sidebar-width-icon))]" 
        )}
        {...props}
      >
        <div className={cn(sidebarVariants({ side, variant }), className)}>
          {children}
        </div>
      </div>
    )
  }
)
Sidebar.displayName = "Sidebar"

